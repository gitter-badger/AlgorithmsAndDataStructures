#include <iostream>


//================================================================================================================================================================================================================================================================
__int64 factorial(__int64 n)
{
    if(n == 0)
    {
        return 1;
    }

    return n * factorial(n - 1);
}

//================================================================================================================================================================================================================================================================
__int64 sum(__int64 n)
{
    if(n == 0)
    {
        return 0;
    }
    return n + sum(n - 1);
}

//================================================================================================================================================================================================================================================================
__int64 sum_notrecursive(__int64 n)
{
    int sum = 0;
    for(int i = 0; i < n; ++i)
    {
        sum += i;
    }

    return sum;
}

//================================================================================================================================================================================================================================================================
int gcd(int m, int n)
{
    if(n == 0)
    {
        return m;
    }

    return gcd(n, m % n);
}



//================================================================================================================================================================================================================================================================
int main()
{
    std::cout << std::endl << gcd(9, 100  );
    std::cout << std::endl;
    return 0;
}
