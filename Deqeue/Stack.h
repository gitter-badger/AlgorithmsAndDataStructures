#pragma once

#include "Deqeue.h"


template<typename T> class Stack
{
	public:	
		//================================================================================================================================================================
		void push(const T& data)
		{
			container.addBack(data);
		}

		//================================================================================================================================================================
		T pop()
		{
			T item = container.getBack();
			container.removeBack();

			return item;
		}

		//================================================================================================================================================================
		T top()
		{
			return container.getBack();
		}

		//================================================================================================================================================================
		bool isEmpty()
		{
			return container.isEmpty();
		}

	private:
		Dequeue<T> container;
};
