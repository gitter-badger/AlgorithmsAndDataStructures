#include "ArrayStack.h"

//================================================================================================================================================================================================================================================================
ArrayStack::ArrayStack(int size) : maxSize(size)
{
    elements = nullptr;
    index = 0;

    reserve(maxSize);
}

//================================================================================================================================================================================================================================================================
ArrayStack::~ArrayStack()
{
    if(elements)
    {
        delete[] elements;
    }
}

//================================================================================================================================================================================================================================================================
void ArrayStack::push(int item)
{
    elements[index++] = item;
}

//================================================================================================================================================================================================================================================================
int ArrayStack::pop()
{
    return elements[--index];
}

//================================================================================================================================================================================================================================================================
void ArrayStack::reserve(int size)
{
    if(elements)
    {
        delete[] elements;
    }
    elements = new int[size];
}

//================================================================================================================================================================================================================================================================
int ArrayStack::top()
{
    return elements[index - 1];
}

//================================================================================================================================================================================================================================================================
bool ArrayStack::isEmpty()
{
    return index == 0;
}
