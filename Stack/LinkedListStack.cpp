#include "LinkedListStack.h"


//================================================================================================================================================================================================================================================================
LinkedListStack::LinkedListStack()
{
	head = nullptr;
}

//================================================================================================================================================================================================================================================================
void LinkedListStack::push(int item)
{
	Node* newNode = new Node(item);

	newNode->next = head;
	head          = newNode;
}

//================================================================================================================================================================================================================================================================
int LinkedListStack::pop()
{
	Node* topNode = head;
	head = head->next;

	return topNode->data;
}

//================================================================================================================================================================================================================================================================
int LinkedListStack::top()
{
	return head->data;
}

//================================================================================================================================================================================================================================================================
bool LinkedListStack::isEmpty()
{
	return !head;
}
